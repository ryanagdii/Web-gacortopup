    <script>
    document.addEventListener("DOMContentLoaded", function () {
    const totalRevenueElement = document.getElementById("totalRevenue");
    const totalSuccessElement = document.getElementById("totalSuccess");
    const totalPendingElement = document.getElementById("totalPending");

    let totalRevenue = 0;
    let totalSuccess = 0;
    let totalPending = 0;

    // Fetch daftar order_id dan harga dari fetch_order_id.php
    fetch("fetch_order_id.php")
        .then(response => response.json())
        .then(orderData => {
            const fetchStatusPromises = orderData.map(order => {
                return fetch(`fetch_status.php?order_id=${order.order_id}`)
                    .then(response => response.json())
                    .then(statusData => {
                        if (statusData.status === "settlement") {
                            totalRevenue += parseInt(order.harga); // Ambil harga dari orderData
                            totalSuccess++;
                        } else if (statusData.status === "pending") {
                            totalPending++;
                        }
                    });
            });

            // Tunggu semua permintaan selesai
            Promise.all(fetchStatusPromises).then(() => {
                // Update tampilan setelah semua data dihitung
                totalRevenueElement.textContent = `Rp ${totalRevenue.toLocaleString("id-ID")}`;
                totalSuccessElement.textContent = totalSuccess;
                totalPendingElement.textContent = totalPending;
            });
        })
        .catch(error => {
            console.error("Error fetching order data:", error);
        });
});

</script>



